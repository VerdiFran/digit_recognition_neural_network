export const setSizes = (elementIds) => (dispatch) => {
    elementIds.forEach(elemId => {
        let elem = document.getElementById(elemId)
        elem.width = CANVAS_WIDTH
        elem.height = CANVAS_HEIGHT
    })
}
export const drawGrid = (elementId) => (dispatch) => {
    const element = document.getElementById(elementId)
    const ctx = element.getContext('2d')
    const pixel = PIXEL_SIZE

    element.width = CANVAS_WIDTH
    element.height = CANVAS_HEIGHT

    const width = element.width
    const height = element.height
    const pixelWidth = width / pixel
    const xStep = width / pixelWidth
    const yStep = height / pixelWidth

    const drawLine = function (x1, y1, x2, y2, color = '#aaaaaa') {
        ctx.beginPath()
        ctx.strokeStyle = color
        ctx.lineJoin = 'miter'
        ctx.lineWidth = 1
        ctx.moveTo(x1, y1)
        ctx.lineTo(x2, y2)
        ctx.stroke()
    }

    for (let x = xStep; x < width; x += xStep) {
        drawLine(x, 0, x, height)
    }

    for (let y = yStep; y < height; y += yStep) {
        drawLine(0, y, width, y)
    }
}
