let initialState = {
    sourcePoints: zeroMatrix.map(row => [...row]),
    resultDigit: ''
}

const recognitionReducer = (state = initialState, action) => {
    switch (action.type) {
        case SET_SOURCE_POINTS:
            return {
                ...state,
                sourcePoints: state.sourcePoints.map((row, idx) =>
                    row.map((point, index) =>
                        idx === action.data[0].y && index === action.data[0].x ? action.data[0].value : point)
                )
            }
        case CLEAR_SOURCE_DATA:
            console.log('clear')
            return {
                ...state,
                sourcePoints: state.sourcePoints.map(row => row.map(() => 0)),
                resultDigit: ''
            }
        case SET_RESULT_DIGIT:
            return {
                ...state,
                resultDigit: action.resultValue
            }
        default:
            return state
    }
}

export const setSourcePoints = (data) => ({type: SET_SOURCE_POINTS, data})
export const clearSourceData = () => ({type: CLEAR_SOURCE_DATA})
const setResultDigit = (resultValue) => ({type: SET_RESULT_DIGIT, resultValue})


export const calculateResult = (sourcePoints) => (dispatch) => {
    dispatch(setResultDigit(network.feedForward(toArray(sourcePoints))))
}

const toArray = (data) => {
    let array = []
    data.forEach(row => row.forEach(item => array.push(item)))
    return array
}

export default recognitionReducer
